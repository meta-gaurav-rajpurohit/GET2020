package system;

public class Vehicle {
    int empId; 
    String vahicleName, vahicleType, vahicleNumber, Identification, currency;
    public int getEmpId() {
        return empId;
    }
    public void setEmpId(int empId) {
        this.empId = empId;
    }
    public String getVahicleName() {
        return vahicleName;
    }
    public void setVahicleName(String vahicleName) {
        this.vahicleName = vahicleName;
    }
    public String getVahicleType() {
        return vahicleType;
    }
    public void setVahicleType(String vahicleType) {
        this.vahicleType = vahicleType;
    }
    public String getVahicleNumber() {
        return vahicleNumber;
    }
    public void setVahicleNumber(String vahicleNumber) {
        this.vahicleNumber = vahicleNumber;
    }
    public String getIdentification() {
        return Identification;
    }
    public void setIdentification(String identification) {
        Identification = identification;
    }
    public String getCurrency() {
        return currency;
    }
    public void setCurrency(String currency) {
        this.currency = currency;
    }
}
